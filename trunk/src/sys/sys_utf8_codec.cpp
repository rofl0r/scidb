bool
Codec::isLatin1(mstl::string const& s) const
{
	for (unsigned i = 0; i < s.size(); ++i)
	{
		unsigned char c = s[i];

		if (c & 0x80)
		{
			if (c < 0xc2 || 0xc3 < c)
				return false;

			c = s[++i];

			if (c < 0x80 || 0xbf < c)
				return false;
		}
	}

	return false;
}


